<h2>Class 5 - CSS3</h2>
<p>In this class we'll learn more CSS3 properties that allow you to do more with our CSS. We'll look at applicable examples of how they are used across the web (<a href="http://www.boagworld.com/" title="Paul Boag uses CSS3 techniques on his web design blog" target="_blank">www.boagworld.com</a> will be one such example). CSS3 is essential because it lightens the amount of markup and images required. In the past rounded corners needed an image, now they are taken care of via CSS3 which lightens page weight and our work load as web designers.
