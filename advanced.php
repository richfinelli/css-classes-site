<div class="class-info">
<h2>Class 3 - RWD: Flexi-Images, Media Queries</h2>
<p>Resonsive Web Design doesn't end with fluid grids. Two other foundations are flexible images and media queries. Flexible images allow our images to expand and contract with screen size, and media queries allow us to add conditional CSS that only gets applied to certain screen sizes. Media queries are an amazing feature of CSS3 that responsive web design could not live without.
<br><br>
Post Class reading: Chapter 5 of Responsive Web Design.
</p>
