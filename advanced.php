<h3 class="separation">Tip of the Week</h3>

<div class="secondary-section">

<p class="tip">To center a box on your web page set its left and right margins to the keyword of "auto" and make sure it has a width. </p>
<p class="tip display-none">Using Class names instead of ID's to rename elements is an easy way to avoid complex specificity problems.</p>
<p class="tip display-none">Using EM's instead of Pixels for font sizing is recommended by the W3C (and me)!</p>
<p class="tip display-none">Think of every HTML element on your web page as a rectangle.</p>

