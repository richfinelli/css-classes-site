<h2>Class 2 - RWD: Fluid Grids</h2>
<p>
Responsive web design allows us to create layouts that adapt to the screen size our sites are being viewed on. It is a great way to optimize your site for mobile, and tablet. The fluid or flexible grid layout is the first pillar of responsive web design. Our layouts will begin to conform to varying screen sizes. 
